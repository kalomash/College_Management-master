package college.management.Database;


public class Subjects {


    private final String sub1;
    private final String sub2;
    private final String sub3;

    public Subjects(String sub1, String sub2, String sub3) {

        this.sub1 = sub1;
        this.sub2 = sub2;
        this.sub3 = sub3;

    }

    public String getSub1() {
        return sub1;
    }

    public String getSub2() {
        return sub2;
    }

    public String getSub3() {
        return sub3;
    }


}
